package Tugas_1;


public class Undika {
    private String nama,alamat;


//setter 
    public void setNama(String nama){
        this.nama=nama;
    }
    
    public void setAlamat(String alamat){
        this.alamat=alamat;
    }
    
    //Getter
    
    public String getNama(){
        return this.nama;
    }
    
    public String getAlamat(){
        return this.alamat;
    }
    
}